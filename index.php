<h1><u>О группе</u></h1>
<p>Электрофорез — декадентские синтипоп-герои новой русской волны.</p>
<p>На данный момент синтипоп-группа «Электрофорез» является одним из наиболее успешных проектов новой волны русской независимой музыки. Популярность коллективу обеспечивает декадентская лирика, особенное внимание к томной и мрачной эстетике, одновременно сдержанный и мощный синтезаторный звук, а также харизма фронтмена Ивана Курочкина.</p>
<p>Состав группы: Иван Курочкин, Виталий Талызин. Тексты и музыку пишут вместе, базируются в Санкт-Петербурге, где и родились. </p>


